import re
from pathlib import Path
from typing import Dict, List, Tuple, Any, Optional

from pypdf import PdfReader, PdfWriter

INVALID_WIN_CHARS = r'<>:"/\\|?*'
RESERVED_WIN_NAMES = {
    "CON","PRN","AUX","NUL",
    *(f"COM{i}" for i in range(10)),
    *(f"LPT{i}" for i in range(10)),
}

def sanitize_filename(name: str, replacement: str = " ") -> str:
    name = "".join((c if c not in INVALID_WIN_CHARS and 31 < ord(c) != 127 else replacement) for c in name)
    name = re.sub(r"\s+", " ", name).strip()
    name = name.rstrip(" .")
    if name.upper() in RESERVED_WIN_NAMES:
        name = f"_{name}_"
    return name or "untitled"

def parse_page_spec(spec: Any) -> List[int]:
    """
    Accepts:
      - "N"  -> page N (1-based)
      - "A-B" -> pages A..B inclusive (1-based), hyphen or en dash
      - int N -> page N (1-based) also supported
    Returns a list of 1-based page numbers.
    """
    if isinstance(spec, int):
        return [spec]
    if isinstance(spec, str):
        s = spec.strip()
        m = re.fullmatch(r"(\d+)\s*[-–]\s*(\d+)", s)
        if m:
            a, b = int(m.group(1)), int(m.group(2))
            if a > b:
                a, b = b, a
            return list(range(a, b + 1))
        m = re.fullmatch(r"(\d+)", s)
        if m:
            return [int(m.group(1))]
    raise TypeError(f"Unsupported page spec: {spec!r}. Use 'N' or 'A-B' (1-based).")

def ensure_dir(path: Path) -> None:
    path.mkdir(parents=True, exist_ok=True)

def ensure_unique_path(path: Path) -> Path:
    """Avoid overwriting if duplicate titles exist."""
    if not path.exists():
        return path
    parent = path.parent
    stem = path.stem
    suffix = path.suffix
    i = 2
    while True:
        candidate = parent / f"{stem} ({i}){suffix}"
        if not candidate.exists():
            return candidate
        i += 1

def extract_numbered_pairs(g: Dict[str, Any]) -> List[Tuple[int, str, Tuple[Tuple[str, Any], ...]]]:
    pdf_pat = re.compile(r"^PDF_NAME_LIST_(\d+)$")
    list_pat = re.compile(r"^list(\d+)$")
    pdf_map: Dict[int, str] = {}
    list_map: Dict[int, Tuple[Tuple[str, Any], ...]] = {}
    for k, v in g.items():
        m1 = pdf_pat.match(k)
        if m1 and isinstance(v, str):
            pdf_map[int(m1.group(1))] = v
        m2 = list_pat.match(k)
        if m2 and isinstance(v, tuple):
            list_map[int(m2.group(1))] = v
    common = sorted(set(pdf_map.keys()) & set(list_map.keys()))
    return [(n, pdf_map[n], list_map[n]) for n in common]

def split_pdf(pdf_path: Path, items: Tuple[Tuple[str, Any], ...], out_dir: Path) -> None:
    reader = PdfReader(str(pdf_path))
    total = len(reader.pages)
    ensure_dir(out_dir)
    for title, spec in items:
        safe_title = sanitize_filename(str(title))
        dest_path = ensure_unique_path(out_dir / f"{safe_title}.pdf")
        pages_1based = parse_page_spec(spec)
        valid_pages_0based: List[int] = []
        for p in pages_1based:
            if 1 <= p <= total:
                valid_pages_0based.append(p - 1)
            else:
                print(f"[WARN] Skipping out-of-range page {p} for '{title}' in {pdf_path.name} (total {total})")
        if not valid_pages_0based:
            print(f"[WARN] No valid pages for '{title}' from spec {spec!r} in {pdf_path.name}; skipping.")
            continue
        writer = PdfWriter()
        for idx in valid_pages_0based:
            writer.add_page(reader.pages[idx])
        with open(dest_path, "wb") as f:
            writer.write(f)
        print(f"[OK] Wrote {dest_path} from pages {pages_1based}")

def main(globals_dict: Optional[Dict[str, Any]] = None) -> None:
    g = globals_dict if globals_dict is not None else globals()
    for n, pdf_filename, items in extract_numbered_pairs(g):
        src = Path(pdf_filename)
        if not src.exists():
            print(f"[ERROR] Missing source PDF: {src}")
            continue
        out_dir = Path(src.stem)  
        ensure_dir(out_dir)
        print(f"[INFO] Processing {src} -> {out_dir}")
        split_pdf(src, items, out_dir)

if __name__ == "__main__":
    main()